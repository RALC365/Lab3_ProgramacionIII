package lab3_programacionii;

public class Personas {
    private String username;
    private String password;
    private String correo_elecronico;
    private String nombre;
    private int id;
    private String fecha_nacimiento;

    public Personas() {
    }

    public Personas(String username, String password, String correo_elecronico, String nombre, int id, String fecha_nacimiento) {
        this.username = username;
        this.password = password;
        this.correo_elecronico = correo_elecronico;
        this.nombre = nombre;
        this.id = id;
        this.fecha_nacimiento = fecha_nacimiento;
    }
    
    
    
    
}
